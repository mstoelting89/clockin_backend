    @Override
    public UserDetails loadUserByUsername(String email) throws UsernameNotFoundException {
        return userRepository.findByEmail(email)
                .orElseThrow(() -> new UsernameNotFoundException("User not found"));
    }

    public User loadUserByMail(String email) {
        return userRepository.findByEmail(email)
                .orElseThrow(() -> new UsernameNotFoundException("User not found"));
    }

    public String signUpUser(User user) {

        // check if user exists
        boolean userExists = userRepository.findByEmail(user.getEmail()).isPresent();
        if (userExists) {
            throw new IllegalStateException("User is bereits vorhanden");
        }

        // encode password
        user.setPassword(bCryptPasswordEncoder.encode(user.getPassword()));

        // save user
        userRepository.save(user);

        // create token
        String tokenId = UUID.randomUUID().toString();

        Token token = new Token(
                tokenId,
                LocalDateTime.now(),
                LocalDateTime.now().plusMinutes(30),
                user
        );

        tokenService.saveToken(token);

        return tokenId;
    }

    public String handleForgotPassword(String email) {
        boolean userExists = userRepository.findByEmail(email).isPresent();
        if (!userExists) {
            throw new IllegalStateException("Email ist nicht vorhanden");
        }

        // get user
        User user = loadUserByMail(email);

        String token = jwtTokenService.generateToken(email);

        String link = "http://localhost:8081/login/forgotpassword?token=" + token;

        emailSender.send(email, buildEmail(user.getFirstName(), link));

        return "done";
    }

    public String resetPassword(String token, String password) {
        String email = jwtAuthenticationService.getEmailFromToken(token);
        User user = loadUserByMail(email);

        // encode password
        user.setPassword(bCryptPasswordEncoder.encode(password));
        userRepository.save(user);
        return "Passwort wurde zur√ºckgesetzt";
    }

    public int enableUser(String email) {
        return userRepository.enableUser(email);
    }

    public int unlockUser(String email) {
        return userRepository.unlockUser(email);
    }

